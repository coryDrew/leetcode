package com.zcy.hash;

public class FirstMissingPositive41 {
    public int firstMissingPositive(int[] nums) {
        //hard题    时间o（n）空间o（1）
        //官解讲的还可以的，认真看吧能看个八九分
        int n = nums.length;
        for (int i = 0; i < n; ++i) {
            if (nums[i] <= 0) {
                nums[i] = n + 1;
            }
        }
        for (int i = 0; i < n; ++i) {
            int num = Math.abs(nums[i]);
            if (num <= n) {
                nums[num - 1] = -Math.abs(nums[num - 1]);
            }
        }
        for (int i = 0; i < n; ++i) {
            if (nums[i] > 0) {
                return i + 1;
            }
        }
        return n + 1;
    }
}
